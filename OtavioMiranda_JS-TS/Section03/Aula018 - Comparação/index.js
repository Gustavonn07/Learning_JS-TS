// Operadores de Comparação

/*

<    maior que
<=   maior ou igual a que
>    menor que
>=   menor ou igual a que
==   igualdade **********
===  igualdade estrita
!=   diferente **********
!==  diferente estrito

*/ 

// Sempre retorna valor booleano
console.log(typeof(5 > 10));
console.log((5 !== 10));
console.log((5 === 10));
console.log((5 === 5));
console.log((5 >= 10));
console.log((5 <= 10));