// Objeto Math

// Arredondando:
let num1 = 9.54578;
let num2 = Math.floor(num1); // Para baixo
let num3 = Math.ceil(num1); // Para cima
let numrounded = Math.round(num1); // O mais perto
console.log(num2);
console.log(num3);
console.log(numrounded);

// Pegar o maior e menor valor de uma sequência:
console.log(Math.max(1,2,3,4,5,6,67,8,10));
console.log(Math.min(1,2,3,4,5,-6,67,8,10));

// Pegar um número aleatorio:
console.log(Math.random());
