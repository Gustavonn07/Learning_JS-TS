let valor1 = 32;
let valor2 = 3;

// Soma
console.log(valor1 + valor2);

// Diminuição
console.log(valor2 - valor1);

// Dvisão
console.log(valor2 / valor1);

// Multiplicação
console.log(valor1 * valor2);

// Potenciação
console.log(valor1 ** valor2);

// Resto da Divisão
console.log(valor2 % valor1);

// Podemos somar ou diminuir 1 valor com ++ ou --

++valor1; // 33
++valor1; // 34
++valor1; // 35
++valor1; // 36

console.log(valor1);

--valor1; // 35
--valor1; // 34
--valor1; // 33 
--valor1; // 32

console.log(valor1);

